class ProductModel {
  final String id;
  final String title;
  final String description;
  final String subTitle;
  final String imageUrl;
  final double price;
  final String color;

  ProductModel({
    required this.id,
    required this.title,
    required this.description,
    required this.subTitle,
    required this.imageUrl,
    required this.price,
    required this.color,
  });
}
